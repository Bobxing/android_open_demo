package com.het.open.lib.model;

import java.util.Arrays;

/**蓝牙历史数据model
 * Created by xuchao on 2016/5/6.
 */
public class BleFileDataModel {
    private int fileId;  //蓝牙数据ID

    private String filePath;//蓝牙数据地址

    private String uploadTime;	//蓝牙数据

   private byte[] file;//蓝牙数据上传时间

    public int getFileId() {
        return fileId;
    }

    public void setFileId(int fileId) {
        this.fileId = fileId;
    }

    public String getFilePath() {
        return filePath;
    }

    public void setFilePath(String filePath) {
        this.filePath = filePath;
    }

    public String getUploadTime() {
        return uploadTime;
    }

    public void setUploadTime(String uploadTime) {
        this.uploadTime = uploadTime;
    }

    public byte[] getFile() {
        return file;
    }

    public void setFile(byte[] file) {
        this.file = file;
    }

    @Override
    public String toString() {
        return "BleFileDataModel{" +
                "fileId=" + fileId +
                ", filePath='" + filePath + '\'' +
                ", uploadTime='" + uploadTime + '\'' +
                ", file=" + Arrays.toString(file) +
                '}';
    }
}
